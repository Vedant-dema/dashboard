for (const code of Object.keys(LANGUAGE_OVERRIDES) as Array<keyof typeof LANGUAGE_OVERRIDES>) {
  MESSAGES[code] = { ...MESSAGES.en, ...LANGUAGE_OVERRIDES[code] };
}

export type LanguageContextType = {
  language: LanguageCode;
  setLanguage: (language: LanguageCode) => void;
  t: (key: string, fallback?: string) => string;
  localeTag: string;
  languageOptions: LanguageOption[];
};

export const LanguageContext = createContext<LanguageContextType | null>(null);

function readLanguageFromAppSettings(): LanguageCode | null {
  try {
    const raw = localStorage.getItem(APP_SETTINGS_KEY);
    if (!raw) return null;
    const parsed = JSON.parse(raw) as { language?: string };
    const code = parsed.language;
    if (code && LANGUAGE_OPTIONS.some((l) => l.code === code)) return code as LanguageCode;
  } catch {
    // ignore
  }
  return null;
}

function getInitialLanguage(): LanguageCode {
  try {
    const stored = localStorage.getItem(STORAGE_KEY) as LanguageCode | null;
    if (stored && LANGUAGE_OPTIONS.some((l) => l.code === stored)) return stored;
    const fromSettings = readLanguageFromAppSettings();
    if (fromSettings) return fromSettings;
  } catch {
    // ignore
  }
  return "de";
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<LanguageCode>(getInitialLanguage);

  const setLanguage = useCallback((next: LanguageCode) => {
    try {
      localStorage.setItem(STORAGE_KEY, next);
    } catch {
      // ignore
    }
    setLanguageState(next);
  }, []);

  useEffect(() => {
    try {
      localStorage.setItem(STORAGE_KEY, language);
    } catch {
      // ignore
    }
    document.documentElement.lang = language;
  }, [language]);

  const value = useMemo<LanguageContextType>(
    () => ({
      language,
      setLanguage,
      t: (key, fallback = key) =>
        MESSAGES[language]?.[key] ?? MESSAGES.en[key] ?? fallback,
      localeTag: localeTagForLanguage(language),
      languageOptions: LANGUAGE_OPTIONS,
    }),
    [language, setLanguage]
  );

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error("useLanguage must be used within LanguageProvider");
  return ctx;
}
